    public function ideaShop()
    {
        $ideas=idea::where('status','=',1)
            ->paginate(20);

        foreach ($ideas as $item)
        {
            $user=User::where('id','=',$item->user_id)->first();
            if($user)
            {
                $item->lname=$user->lname;
            }
        }
        return view('acckt_master.pages.shop')
            ->with('ideas',$ideas);
    }
